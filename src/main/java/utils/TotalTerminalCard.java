package utils;

public class TotalTerminalCard {
	
	public String terminalId;

	public Float totalMasterVisa;
	public Float totalOthers;
	public Float total;
	
	public String getTerminalId() {
		return terminalId;
	}
	public void setTerminalId(String terminalId) {
		this.terminalId = terminalId;
	}
	public Float getTotalMasterVisa() {
		return totalMasterVisa;
	}
	public void setTotalMasterVisa(Float totalMasterVisa) {
		this.totalMasterVisa = totalMasterVisa;
	}
	public Float getTotalOthers() {
		return totalOthers;
	}
	public void setTotalOthers(Float totalOthers) {
		this.totalOthers = totalOthers;
	}
	public Float getTotal() {
		return total;
	}
	public void setTotal(Float total) {
		this.total = total;
	}
	
	


}
